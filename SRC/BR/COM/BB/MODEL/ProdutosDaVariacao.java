package br.com.bb.model;

/**
 *
 * @author F2872117
 */
public class ProdutosDaVariacao {
    
    private String codigo;
    private String insumo;
    private String area;
    private String nome;
    private String quantidade;
    private String previsaoSafra;
    private String vlrItemFinaciado;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getInsumo() {
        return insumo;
    }

    public void setInsumo(String insumo) {
        this.insumo = insumo;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }

    public String getPrevisaoSafra() {
        return previsaoSafra;
    }

    public void setPrevisaoSafra(String previsaoSafra) {
        this.previsaoSafra = previsaoSafra;
    }

    public String getVlrItemFinaciado() {
        return vlrItemFinaciado;
    }

    public void setVlrItemFinaciado(String vlrItemFinaciado) {
        this.vlrItemFinaciado = vlrItemFinaciado;
    }
    
    
    
    
    
    
    
}
